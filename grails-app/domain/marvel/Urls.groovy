
package marvel

class Urls implements Serializable {

     String type;

     String url;

     String getType ()
    {
        return type;
    }

     void setType (String type)
    {
        this.type = type;
    }

     String getUrl ()
    {
        return url;
    }

     void setUrl (String url)
    {
        this.url = url;
    }
}
